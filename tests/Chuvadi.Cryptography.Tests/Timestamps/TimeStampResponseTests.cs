    [Fact]
    public void Decode_GrantedResponse_RecognisesStatus()
    {
        TimeStampResponse r = TimeStampResponse.Decode(Convert.FromHexString(GrantedResponseHex));
        r.Status.Should().Be(TimeStampStatus.Granted);
        r.IsGranted.Should().BeTrue();
    }

    [Fact]
    public void Decode_GrantedResponse_CarriesATimeStampToken()
    {
        TimeStampResponse r = TimeStampResponse.Decode(Convert.FromHexString(GrantedResponseHex));
        r.TimeStampToken.Should().NotBeNull();
        r.TimeStampToken!.TstInfo.Version.Should().Be(1);
    }

    [Fact]
    public void Decode_NullInput_Throws()
    {
        Action act = () => TimeStampResponse.Decode(null!);
        act.Should().Throw<ArgumentNullException>();
    }

    [Fact]
    public void IsGranted_FalseWhenNoToken()
    {
        TimeStampResponse r = new(
            TimeStampStatus.Rejection,
            new[] { "no" },
            timeStampToken: null);
        r.IsGranted.Should().BeFalse();
    }
